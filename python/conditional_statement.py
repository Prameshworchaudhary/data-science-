# two types of operator
#1. relational operator
# <,>,<=,>=,==,!=
a=10
b=20
print(a>b) #output:false
print(b>a) #output:true
print(a<=b) #output:true
print(a>=b)#output:false
print(a==b)#output:false
print(a!=b) #output:true
 # 2. logical operator
 # and (&&) , or (||) , not (!)
x=10
y=20
z=30
print((x>y)and(y>z)) #output: false
print((x<y)or(y<z)) #output: true
 